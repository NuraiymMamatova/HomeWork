
    public class Fish {
        private String fishName;
        private String fishFilm;
        private String fishColor;
        private String fishSpecies;
        private String fishHome;

        public String getFishName() {
            return fishName;
        }

        public void setFishName(String fishName) {
            this.fishName = fishName;
        }
        String getFishFilm(){
            return fishFilm;
        }

        public void setFishFilm(String fishFilm) {
            this.fishFilm = fishFilm;
        }
        String getFishColor(){
            return fishColor;
        }

        public void setFishColor(String fishColor) {
            this.fishColor = fishColor;
        }
        String getFishSpecies(){
            return fishSpecies;
        }

        public void setFishSpecies(String fishSpecies) {
            this.fishSpecies = fishSpecies;
        }
        String getFishHome(){
            return fishHome;
        }

        public void setFishHome(String fishHome) {
            this.fishHome = fishHome;
        }
    }


